# הדפסת לוח הכפל
def mul_board(x,y):
    for i in range(1,x+1):
        for j in range(1,y+1):
            print (i*j,end=' ')
        # ירידת שורה לאחר הדפסת כל שורה בלוח הכפל
        print('')
mul_board(10,10)

